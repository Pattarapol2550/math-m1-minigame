const ENEMY_MAP: Record<string, React.FC<{S:number}>> = {
  dragon: Dragon,
  goblin: Goblin,
  zombie: Zombie,
  skeleton: SkeletonKing,
  lizard: LizardMage,
};

// Map stage enemy names/emojis to sprite keys
export function resolveType(type: string): string {
  const t = type.toLowerCase();
  if (t.includes("dragon") || t.includes("มังกร")) return "dragon";
  if (t.includes("goblin") || t.includes("โกบลิน") || t.includes("ปีศาจ")) return "goblin";
  if (t.includes("zombie") || t.includes("ซอมบี้")) return "zombie";
  if (t.includes("skeleton") || t.includes("บอส") || t.includes("ราชา") || t.includes("กษัตริย์")) return "skeleton";
  return "lizard";
}

export default function EnemySprite({ type, size = 64, className, style }: Props) {
  const key = resolveType(type);
  const Comp = ENEMY_MAP[key] ?? LizardMage;
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      style={{ imageRendering: "pixelated", ...style }}
      className={className}
    >
      <Comp S={size / 16} />
    </svg>
  );
}
